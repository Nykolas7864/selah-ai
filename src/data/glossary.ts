export function findGlossaryEntry(name: string): GlossaryEntry | undefined {
  const lower = name.toLowerCase();
  return GLOSSARY_ENTRIES.find(
    (e) =>
      e.name.toLowerCase() === lower ||
      e.alternateNames.some((alt) => alt.toLowerCase() === lower)
  );
}

export function getAllGlossaryNames(): string[] {
  const names: string[] = [];
  for (const entry of GLOSSARY_ENTRIES) {
    names.push(entry.name);
    for (const alt of entry.alternateNames) {
      names.push(alt);
    }
  }
  // Sort longest first so "John the Baptist" matches before "John"
  return names.sort((a, b) => b.length - a.length);
}
